def brain_init(vault: Path | None = None, *, champion: str = "Lightfather") -> dict[str, Any]:
    root = vault or resolve_vault_root()
    root.mkdir(parents=True, exist_ok=True)
    lygo = root / "lygo"
    lygo.mkdir(exist_ok=True)
    created = []
    for name, body in _seed_templates(champion).items():
        p = root / name
        if not p.is_file():
            p.write_text(body, encoding="utf-8")
            created.append(name)
    for d in DIRS:
        (root / d).mkdir(parents=True, exist_ok=True)
    meta = {"signature": SIGNATURE, "vault": str(root), "created": created, "champion": champion}
    (lygo / "brain_manifest.json").write_text(json.dumps(meta, indent=2), encoding="utf-8")
    log_event("godmode_brain_init", detail=meta)
    return {"ok": True, "vault": str(root), "created": created, "signature": SIGNATURE}


def _p0_write(path: Path, text: str, gate: P0Gatekeeper) -> bool:
    v = gate.validate(text)
    if v.get("verdict") == "QUARANTINE":
        return False
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(text, encoding="utf-8")
    return True


def brain_dream(vault: Path | None = None, *, online_gap: bool = False) -> dict[str, Any]:
    root = vault or resolve_vault_root()
    if not (root / "memory.md").is_file():
        brain_init(root)
    key = resolve_key_root(root)
    hermes = (key / "data" / "hermes_audit" / "audit_trail.log") if key else root / "lygo" / "hermes_audit.log"
    set_log_path(hermes)
    gate = P0Gatekeeper()
    actions: list[str] = []

    notes = root / "notes.md"
    if notes.is_file():
        raw = notes.read_text(encoding="utf-8")
        if "## to-process" in raw and len(raw) > 80:
            actions.append("inbox_scan_ok")

    gap_script = None
    if key:
        gap_script = key / "scripts" / "ascension_gap_filler.py"
    if gap_script and gap_script.is_file():
        proc = subprocess.run(
            [sys.executable, str(gap_script)],
            cwd=str(key),
            capture_output=True,
            text=True,
            timeout=120,
            env={**os.environ, "LYGO_BUILDER_KEY_ROOT": str(key)},
        )
        actions.append(f"gap_filler_exit_{proc.returncode}")
    else:
        actions.append("gap_filler_skipped")

    brief = root / "brief.md"
    stamp = time.strftime("%Y-%m-%d %H:%M:%S")
    block = f"\n\n## Loop dream ({stamp})\n- Vault: `{root}`\n- Actions: {', '.join(actions)}\n"
    if brief.is_file():
        _p0_write(brief, brief.read_text(encoding="utf-8") + block, gate)
    log_event("godmode_brain_dream", detail={"actions": actions, "vault": str(root)})
    return {"ok": True, "vault": str(root), "actions": actions, "online_gap": online_gap}


def brain_brief(vault: Path | None = None) -> dict[str, Any]:
    root = vault or resolve_vault_root()
    p = root / "brief.md"
    if not p.is_file():
        return {"ok": False, "error": "no brief.md — run brain-dream first"}
    return {"ok": True, "brief": p.read_text(encoding="utf-8")[-8000:]}


def brain_query(question: str, vault: Path | None = None) -> dict[str, Any]:
    root = vault or resolve_vault_root()
    if not root.is_dir():
        return {"ok": False, "verdict": "Lattice Gaps Detected", "error": "no vault"}
    hits: list[dict[str, str]] = []
    q = question.lower()
    for md in root.rglob("*.md"):
        if md.name.startswith("."):
            continue
        try:
            text = md.read_text(encoding="utf-8", errors="replace")
        except OSError:
            continue
        if q in text.lower() or any(w in text.lower() for w in q.split() if len(w) > 4):
            hits.append({"path": str(md.relative_to(root)), "snippet": text[:400]})
    if not hits:
        return {"ok": True, "verdict": "Lattice Gaps Detected", "hits": [], "question": question}
    return {"ok": True, "verdict": "GROUNDED", "hits": hits[:12], "question": question}