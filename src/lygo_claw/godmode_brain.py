def resolve_vault_root() -> Path:
    env = os.environ.get("LYGO_BRAIN_VAULT")
    if env:
        return Path(env)
    key = os.environ.get("LYGO_BUILDER_KEY_ROOT")
    if key:
        return Path(key) / "data" / "memory_mycelium" / "vault"
    return Path(__file__).resolve().parents[2] / "data" / "godmode_vault"


def resolve_key_root(vault: Path) -> Path | None:
    key = os.environ.get("LYGO_BUILDER_KEY_ROOT")
    if key:
        return Path(key)
    # vault is .../LYGO_BUILDER_KEY/data/memory_mycelium/vault
    p = vault
    for _ in range(4):
        if (p / "verify_bootstrap.py").is_file():
            return p
        p = p.parent
    return None


